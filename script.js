// Create computer logic. Computer needs to generate random number between 0 and 3 and return either Rock, Paper or Scissors.
function getRockPaperScissors() {
    const randomNumber = Math.floor(Math.random() * 3);
    if (randomNumber === 0) {
        return 'scissors'
    } else if (randomNumber === 1) {
        return 'rock'
    } else if (randomNumber === 2) {
        return 'paper'
    }

}


